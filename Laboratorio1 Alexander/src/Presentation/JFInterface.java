    private void jButtonValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValidateActionPerformed
        // TODO add your handling code here:
        
        if (validateInputs()) {
            Client targetClient = this.database.searchClientByPhone(this.jTextFieldSinpeMovilNumber.getText());
            Transaction transaction = new Transaction(selectedClient,
                    targetClient,
                    Double.parseDouble(this.jTextFieldAmount.getText()),
                    this.jTextFieldDetails.getText(),
                    this.jComboBoxAccountSelection.getSelectedIndex());
            try {
                if (targetClient != null) {
                    if (this.binnacle.binnacleWriter(transaction.getVoucher())) {
                    transaction.getVoucher().printVoucher();
                    this.jLabelAccountNumber.setText(this.selectedClient.getAccounts().get(0).getAccountNumber());
                    this.jLabelBalance.setText("â‚¡ "+this.selectedClient.getAccounts().get(0).getBalance());
                    }else JOptionPane.showMessageDialog(null, "ERROR WHILE CREATING THE VOUCHER", "ERROR", JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "CLIENT NOT FOUND", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException ex) {
                Logger.getLogger(JFInterface.class.getName()).log(Level.SEVERE, null, ex);
            }
   
        }else{
            JOptionPane.showMessageDialog(null, "EMPTY FIELDS", "WARNING", JOptionPane.INFORMATION_MESSAGE);
           
        }  
    }//GEN-LAST:event_jButtonValidateActionPerformed

