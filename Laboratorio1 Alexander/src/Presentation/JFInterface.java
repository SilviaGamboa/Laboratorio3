    private String[] loadAccounts(){
        ArrayList<String> accString = new ArrayList<String>();
        for (int i = 0; i < this.selectedClient.getAccounts().size(); i++) {
            accString.add(this.selectedClient.getAccounts().get(i).getAccountNumber());
        }
        return Arrays.copyOf(accString.toArray(), accString.toArray().length, String[].class);
    }
    
    private String[] loadUsers(){
        ArrayList<String> userString = new ArrayList<String>();
        for (int i = 0; i < this.database.getClients().size(); i++) {
            userString.add(this.database.getClients().get(i).getId() + " " + this.database.getClients().get(i).getName());
        }
        return Arrays.copyOf(userString.toArray(), userString.toArray().length, String[].class);
    }
    
